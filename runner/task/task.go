// Package task определяет контракты и структуры данных для сущностей,
// выполняемых внутри воркер-пула (runner).
package task

// Task представляет собой интерфейс для любой задачи, которая может быть
// запущена асинхронно. Любой тип, реализующий метод Run, удовлетворяет этому интерфейсу.
type Task interface {
	// Run выполняет основную логику задачи.
	// Принимает вариативный список аргументов произвольного типа (args)
	// и возвращает структуру Completed, содержащую информацию о результате выполнения.
	Run(args ...interface{}) Completed
}

// Completed инкапсулирует результат выполнения задачи.
// Служит для передачи статуса завершения обратно в вызывающий компонент.
type Completed struct {
	// Err содержит ошибку, возникшую во время выполнения задачи.
	// Если задача завершилась успешно, значение Err должно быть равно nil.
	Err error
}
